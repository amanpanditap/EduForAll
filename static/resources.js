getBooks = () => {
    let board = document.querySelector('#board').value;
    let medium = document.querySelector('#medium').value;
    let acadYear = document.querySelector('#acadYear').value;
    //let subject = document.querySelector('#subject').value;
    let booksToShow = []

    for (let i = 0; i < allBooks.length; i++) {
        //console.log(allBooks[i].Board, board)
        //console.log(allBooks[i].Class, Number(acadYear))
        //console.log(allBooks[i].Medium, medium)
        if (allBooks[i].Board === board && allBooks[i].Class === Number(acadYear) && allBooks[i].Medium === medium) {
            booksToShow.push(allBooks[i]);
        }
    }

    let content = '';

    booksToShow.forEach(p => {
        content += `
        <div class="col-sm-3">
        <div class="card">
          <div class="card-body">
            <h5 class="card-title" style="text-align: center">${p.Title}</h5>
            <p class="card-text" style="text-align: center">Board: ${p.Board}<br>Class: ${p.Class}<br>Medium: ${p.Medium}</p>
            <div class="text-center">
            <a href=${p.Location} class="btn btn-primary" style="text-align: center">Download</a>
            </div>
          </div>
        </div>
      </div>
  `
    });
    
    document.querySelector("#wantedBooks").innerHTML = content;

}